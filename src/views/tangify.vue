        <h1>Proposed Solution- Tangify</h1>
        <p>
          Tangify is a scalable interactive tool to simply repeat concepts to
          kids which will help them keep learning even when a supervisor is
          unable to engage with them.
        </p>
        <p>
          Tangify consists of a compartmentalized platform to hold tokens of
          everyday objects. The platform is connected to a computer to provide
          the audio and visual cues. The compartments can be mapped to
          properties of the token. When the tokens are placed in slots of the
          platform, the computer gives audio-visual output about that particular
          property of the token.
        </p>
        <p>
          For the prototype,we used fruit tokens to teach the children about
          fruits using artificial fruit as tokens. The platform has four slots
          corresponding to the name, the colour, the spelling and outline of the
          fruit. The children pick a fruit and place it in one of the 4 slots.
          Based on that, an animated video is played showing the corresponding
          property of the fruit.
        </p>
